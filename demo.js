let currentQuestion = 0;

let selectedAnswer = null;


// ======================

function loadQuestion(){

const q = demo[currentQuestion];

document.getElementById(
"questionNumber"
).innerText =
currentQuestion + 1;

document.getElementById(
"question"
).innerHTML =
q.question;

const optionsDiv =
document.getElementById(
"options"
);

optionsDiv.innerHTML = "";

q.options.forEach((option,index)=>{

const div =
document.createElement(
"div"
);

div.classList.add(
"option"
);

div.innerHTML = option;

div.onclick = ()=>{

selectedAnswer = index;

const allOptions =
document.querySelectorAll(
".option"
);

allOptions.forEach(o=>

o.classList.remove(
"selected"
)

);

div.classList.add(
"selected"
);

};

optionsDiv.appendChild(div);

});

}


// ======================

function nextQuestion(){

if(selectedAnswer === null){

alert(
"Pilih jawaban dulu"
);

return;

}

const q = demo[currentQuestion];

document.getElementById(
"explanationBox"
).style.display =
"block";

document.getElementById(
"explanationText"
).innerText =
q.explanation;

}


// ======================

function continueQuiz(){

currentQuestion++;

selectedAnswer = null;

document.getElementById(
"explanationBox"
).style.display =
"none";

if(currentQuestion < demo.length){

loadQuestion();

}else{

alert(
"Demo selesai 🎉"
);

window.location.href =
"index.html";

}

}


